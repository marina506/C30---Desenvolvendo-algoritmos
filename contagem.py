numeros = [10,20,30,20,40,20,50]
print("Lista:", numeros)

# len: verifica número de elementos
print("Comprimento:", len(numeros))

#count: conta ocorrencias
print("Quantas vezes 20 aparece:", numeros.count(20))

#encontra a poisção
print("Ìndice de 30:", numeros.index(30))

#in: verifica a exixtencia
print("20 está na lista?", 20 in numeros)
print("100 está na lista?", 100 in numeros)